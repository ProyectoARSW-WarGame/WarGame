package edu.eci.arsw.warGame.model;

public class Region {

    String id;
    String dueño;
    String canttropas;

    public String getId() {
        return id;
    }

    public String getDueño() {
        return dueño;
    }

    public String getCanttropas() {
        return canttropas;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setDueño(String dueño) {
        this.dueño = dueño;
    }

    public void setCanttropas(String canttropas) {
        this.canttropas = canttropas;
    }
}
